def render_frame(frame, palette, scale=SCALE):
    rows = len(frame)
    cols = len(frame[0])
    img  = Image.new("RGB", (cols * scale, rows * scale), TRANSPARENT)
    px   = img.load()
    for y, row in enumerate(frame):
        for x, code in enumerate(row):
            if code == 0:
                color = TRANSPARENT
            elif code == 8:
                color = ACCENT_COLOR
            else:
                color = palette.get(code, (255, 0, 0))
            for dy in range(scale):
                for dx in range(scale):
                    px[x * scale + dx, y * scale + dy] = color
    return img


def get_frames(animation, costume, scale=SCALE):
    palette    = COSTUMES.get(costume, COSTUMES["orange"])
    raw_frames = ANIMATIONS.get(animation, ANIMATIONS["idle"])
    return [render_frame(f, palette, scale) for f in raw_frames]
